    /// <summary>
    /// 根据类型获得当前位置最右边的士兵
    /// </summary>
    /// <param name="type"></param>
    /// <returns></returns>
    public SpriteSoldier getFocusSoldierByType(int type)
    {
        List<SpriteSoldier> list = new List<SpriteSoldier>();
        for (int i = 0; i < soilders.Count; i++)
        {
            SpriteSoldier ss = soilders[i];
            if (ss.soldier.type == type)
            {
                list.Add(ss);
            }
        }
        SpriteSoldier temp=null;
        if(list.Count>0)
            temp = list[0];
        for (int i = 1; i < list.Count; i++)
        {
            SpriteSoldier ss = list[i];
            if (temp.transform.position.x < ss.transform.position.x)
            {
                temp = ss;
            }
        }
        return temp;
    }
